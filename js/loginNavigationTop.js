function getElementById(id) {
  return document.getElementById(id);
}
function getElementsByClassName(className) {
  return document.getElementsByClassName(className);
}
/*轮播*/
function lunbo() {
  var nacigationHeaderBottomImgs = getElementsByClassName("nacigation-header-bottom-img");
  var nacigationHeaderBottomIndexs = getElementsByClassName("nacigation-header-bottom-index")
  // alert(nacigationHeaderBottomImgs.length);
  var count = 0;
  setInterval(function () {
    if (count == nacigationHeaderBottomIndexs.length) {
      count = 0;
    }
    for (var i=0;i<nacigationHeaderBottomIndexs.length;i++) {
      nacigationHeaderBottomImgs[i].style.display = "none";
      nacigationHeaderBottomIndexs[i].style.backgroundColor="#888";
    }
    nacigationHeaderBottomImgs[count].style.display = "block";
    nacigationHeaderBottomIndexs[count].style.backgroundColor = "#ddd";
    count++;
  },3000);
}
